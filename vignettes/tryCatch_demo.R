## ------------------------------------------------------------------------
f <- function(x) {
  if (x == 42) stop("x cannot be 42")
  x
}

## ------------------------------------------------------------------------
xs <- c(1,2,4,8,16,32,42,64)
for (x in xs) {
  tryCatch(
    print(f(x)), 
    error = function(x) { print("skipped x") } 
  )
}

