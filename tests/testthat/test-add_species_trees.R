context("add_species_trees")

test_that("add_species_trees: use", {
  filename <- "test-add_species_trees.RDa"
  save_parameters_to_file(
    rng_seed = 42,
    sirg = 0.5,
    siri = 0.5,
    scr = 0.5,
    erg = 0.5,
    eri = 0.5,
    age = 5,
    n_species_trees_samples = 1,
    mutation_rate = 0.1,
    n_alignments = 1,
    sequence_length = 10,
    mcmc_chainlength = 10000,
    n_beast_runs = 1,
    filename = filename
  )
  add_pbd_output(filename)
  expect_equal(
    is.na(read_file(filename)$species_trees_with_outgroup[1]),
    TRUE
  )
  add_species_trees(
    filename = filename,
    verbose = FALSE
  )
  expect_equal(
    is.na(read_file(filename)$species_trees_with_outgroup[1]),
    FALSE
  )
  file.remove(filename)
  expect_equal(file.exists(filename), FALSE)

})


test_that("add_species_trees: abuse", {
  expect_error(
    add_species_trees(
      filename = find_path("toy_example_1.RDa"),
      verbose = "TRUE not FALSE"
    ),
    "add_species_trees: verbose should be TRUE or FALSE"
  )

  expect_error(
    add_species_trees(filename = "inva.lid"),
    "add_species_trees: invalid file"
  )

  filename <- "test-add_species_trees.RDa"
  save_parameters_to_file(
    rng_seed = 42,
    sirg = 0.5,
    siri = 0.5,
    scr = 0.5,
    erg = 0.5,
    eri = 0.5,
    age = 5,
    n_species_trees_samples = 1,
    mutation_rate = 0.1,
    n_alignments = 1,
    sequence_length = 10,
    mcmc_chainlength = 10000,
    n_beast_runs = 1,
    filename = filename
  )
  expect_error(
    add_species_trees(filename = filename),
    paste0("add_species_trees: file '", filename, "' needs a pbd_output")
  )
})
